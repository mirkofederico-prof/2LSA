//WHILE ciclo 10 iterazioni
#include <iostream>

using namespace std;

int main()
{

int x = 0;

    cout << "Inizio ciclo" << endl;

    while (x < 10)
    {
        cout << "x e': " << x << endl;
        x ++;
        cout << "x adesso e': " << x << endl;
    }

    cout << "ciclo finito" << endl;
}
